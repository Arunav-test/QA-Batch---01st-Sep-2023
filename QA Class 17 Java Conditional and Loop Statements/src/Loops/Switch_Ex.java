package Loops;

public class Switch_Ex {

	public static void main(String[] args) {
	
		char grade='b';
		
		
		switch(grade)
		{
		case 'A':
			System.out.println("Good performance");
			break;
		case 'B':
			System.out.println("better performance");
			break;
		case 'C':
			System.out.println("Needs improvement ");
			break;
		default:
			System.out.println("invalid grade");
		
		
		}
		

	}

}
